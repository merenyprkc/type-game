export type TranslationKey = keyof typeof TRANSLATIONS.tr;

interface I18nState {
  uiLang: UILang;
  setUILang: (lang: UILang) => void;
  t: (key: TranslationKey) => string;
}

const makeT = (lang: UILang) =>
  (key: TranslationKey): string => TRANSLATIONS[lang][key] as string;

export const useI18n = create<I18nState>()(
  persist(
    (set) => ({
      uiLang: 'tr' as UILang,
      t: makeT('tr'),
      setUILang: (lang) => set({ uiLang: lang, t: makeT(lang) }),
    }),
    {
      name: 'typra-i18n',
      // t is a function — not serializable; rebuild it after hydration
      onRehydrateStorage: () => (state) => {
        if (state) state.t = makeT(state.uiLang);
      },
    }
  )
);
